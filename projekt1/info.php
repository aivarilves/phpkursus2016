<h2>Programmi "Numeroloogia" infofail</h2>

<b>Millega tegu & kuidas kasutada</b><br>
Programm arvutab sünnikuupäeva ja nime baasil välja inimese numeroloogilise info.<br>
Programmi sisenemisel kuvatakse ekraanile ankeet, kus on 
lahti seletatult kirjas, mis lahtrid tuleb täita ja mis on selle eesmärk. 
Täites ankeedi ja vajutades "vaata infot" nuppu, genereerib programm 
vastavalt sisestatud andmetele vajaliku info.<br>
Uue info sisestamiseks tuleks liikuda algusesse ning seejärel ilmub uus 
ankeet uute andmetega ning sisestades lahtritesse uued andmed, 
genereeritakse uus info.<br>
